#MUESTRA PRIMER NOMBRE, APELLIDO Y SALARIO DE LOS TRABAJADORES CUYO SALARIO NO ESTA DENTRO DEL RANGO

SELECT FIRST_NAME,LAST_NAME,SALARY FROM Employees 
WHERE salary NOT BETWEEN 10000 AND 15000


#MUESTRA PRIMER NOMBRE, APELLIDO Y DEPARTAMENTO DE LOS	TRABAJADORES CUYO DEPARTAMENTO QUE ESTAN EN EL DEPARTAMENTO 30 Y 100

SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_ID FROM Employees 
WHERE DEPARTMENT_ID IN (30, 100) 
ORDER BY DEPARTMENT_ID ASC;


#MUESTRA PRIMER NOMBRE, APELLIDO, SALARIO Y DEPARTAMENTO DE LOS TRABAJADORES CUYO SALARIO NO ESTA DENTRO DEL RANGO
#Y  QUE ESTAN EN EL DEPARTAMENTO 30 Y 100


SELECT FIRST_NAME, LAST_NAME, SALARY, DEPARTMENT_ID FROM Employees
WHERE SALARY NOT BETWEEN 10000 AND 15000
AND DEPARTMENT_ID IN (30, 100);


#MUESTRA PRIMER NOMBRE, APELLIDO Y LA FECHA DE CONTRATACION QUE SEA IGUAL 1987

SELECT FIRST_NAME, LAST_NAME, HIRE_DATE FROM Employees 
WHERE YEAR(HIRE_DATE) = 1987;


#MUESTRA PRIMER NOMBRE DEL TRABAJADOR CUYO NOMBRE CONTENGA LAS LETRAS B Y C

SELECT FIRST_NAME FROM Employees 
WHERE FIRST_NAME LIKE '%b%' AND FIRST_NAME LIKE '%c%';


#MUESTRA APELLIDO DEL TRABAJADOR CUYA CLAVE DE SU TRABAJO SEA IT_PROG  Y   SH_CLERK

SELECT LAST_NAME, JOB_ID, SALARY FROM Employees 
WHERE JOB_ID IN ("IT_PROG", "SH_CLERK") 
AND SALARY NOT IN (4500,10000, 15000);


#MUESTRA APELLIDO DEL TRABAJADOR CUYA DIMENSION SEA DE	6 CARACTERES

SELECT LAST_NAME FROM Employees WHERE LENGTH(LAST_NAME)=6;


#MUESTRA APELLIDO DEL TRABAJADOR CUYA TERCERA LETRA SEA E

SELECT LAST_NAME FROM Employees WHERE LAST_NAME LIKE '__e%'


